    <h2>My bachelor thesis dissertation</h2>
    <p><b>Tor och webbplatsorakel: Konstruktion och utvärdering av webbplatsorakel från DNS-tidtagning i Tor-nätverket</b></p>
    <p>
        <q cite='http://urn.kb.se/resolve?urn=urn:nbn:se:kau:diva-82564'>
            <b>Abstract [en]</b>
            This paper discsusses the question: is website oracles in Tor from timing DNS something we have to worry about?
            This paper builds apon the findings done by Rasmus Dahlberg and Tobias Pulls in thier paper Website Fingerprinting with Website Oracles.
            A website oracle is a side channel attack that answers the predicate: has this website been visited before?
            The website oracle is constructed and test are carried out, with great outcome, resulting in that website oracles from timing DNS is not an attack that puts individuals using Tor at risk, but certanly challanges the idea of an anonymity network when such a lot of data can be derived from DNS.
        </q>
